public class LocalVar {
    public static void main (String[] args) {
        // byte can only contain value from -128 to 127.
        byte b1 = -128;
        System.out.println(b1);
        byte b2 = 127;
        System.out.println(b2);

        // 
        short s1 = -32768;
        System.out.println(s1);
        short s2 = 32767;
        System.out.println(s2);
    }

}
